
public class HelloWorld{
    public static void main(String args[]){
        
        // prints message on a new line
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        
        // prints on the current active line
        System.out.print("Hello World");
        System.out.print("Hello World");
        System.out.print("Hello World");
        
    }
}